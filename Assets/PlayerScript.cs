using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class PlayerScript : MonoBehaviour
{
    [SerializeField] private float horizontalSpeed;
    [SerializeField] private  Rigidbody2D rb;
    [SerializeField] private Animator anim;
    [SerializeField] private int jumpForce;
    [SerializeField] private int jumpSpeed;
    [SerializeField] private CheckGround checkground;
    public bool isAttack;
    bool ismovenow = false;
    private float interval;
    private float time = 0f;//経過時間
  
    // Start is called before the first frame update
    void Start()
    {
        rb = GetComponent<Rigidbody2D>();
        anim = GetComponent<Animator>();
        interval = 0.5f;//時間間隔

        //this.gameObject.SetActive(false);
       // anim.enabled = false;
    }
    // Update is called once per frame
    void FixedUpdate()
    {
        float horizontalkey = Input.GetAxisRaw("Horizontal");
        //attackがfalseの時以下が動く
        if (!isAttack)
        {
            //右入力で右向きに動く
            if (horizontalkey > 0)
            {
                ismovenow = true;
                transform.rotation = new Quaternion(0, 0, 0, 0);
                rb.velocity = new Vector2(horizontalSpeed, rb.velocity.y);
                anim.SetBool("isRun", true);
            }
            //左入力で左向きに動く
            else if (horizontalkey < 0)
            {
                ismovenow = true;
                transform.rotation = new Quaternion(0, 180f, 0, 0); //左入力で左向きに反転
                rb.velocity = new Vector2(-horizontalSpeed, rb.velocity.y);
                anim.SetBool("isRun", true);
            }
            //ボタンを離すと止まる
            else if (ismovenow)
            {
                ismovenow = false;
                rb.velocity = Vector2.zero;
                anim.SetBool("isRun", false);
            }
        }
    }
    void Update()
    {
        time += Time.deltaTime;
        if (time > interval)//マウスを左クリックした時
        {
            //左クリック入力でattackする
            if (Input.GetButtonDown("Fire1"))
            {
                anim.SetBool("attack", true);
                isAttack = true;

                time = 0;//時間リセット
            }
        }
        Jump();
    }
    void Jump()
    {
        bool jumpKey = Input.GetButtonDown("Jump");
       
        if (jumpKey)
        {
            if (checkground.GetCheckGround())
            {
                rb.AddForce(Vector2.up * jumpForce, ForceMode2D.Impulse); //ジャンプの計算
                anim.SetBool("isJump", true);
            }
        } 
    }
    public void Land()
    {
        anim.SetBool("isJump", false);
    }
    private void IsAttackFalse() 
    {
        //アニメーションイベントで呼び出す
        isAttack = false;
    }

}
